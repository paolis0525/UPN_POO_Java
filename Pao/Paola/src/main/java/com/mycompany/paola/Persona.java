package com.mycompany.paola;

/**
 *
 * @author pao
 */
public class Persona {
    int edad;
    String sexo;
    String nombre;

    public Persona() {
    }

    public Persona(int edad, String sexo, String nombre) {
        this.edad = edad;
        this.sexo = sexo;
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
    
}
